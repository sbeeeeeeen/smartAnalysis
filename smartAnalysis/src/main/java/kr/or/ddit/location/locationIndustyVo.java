package kr.or.ddit.location;

public class locationIndustyVo {
	
	private String code;			//업종분류 코드
	private String name;			//업종분류 이름
	private String rank;			//업종 등급
	private char parentIndustry;	//소분류 대분류
	private int cnt;				//업체 개수
	
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public char getParentIndustry() {
		return parentIndustry;
	}
	public void setParentIndustry(char parentIndustry) {
		this.parentIndustry = parentIndustry;
	}
	
	
	
	
	
}
