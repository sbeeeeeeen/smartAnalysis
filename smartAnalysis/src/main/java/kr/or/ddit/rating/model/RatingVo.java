package kr.or.ddit.rating.model;

import java.util.Date;

public class RatingVo {
	private int rt_num;
	private long reg_code;
	private double rt_avg;
	private Date rt_month;
	public int getRt_num() {
		return rt_num;
	}
	public void setRt_num(int rt_num) {
		this.rt_num = rt_num;
	}
	public long getReg_code() {
		return reg_code;
	}
	public void setReg_code(long reg_code) {
		this.reg_code = reg_code;
	}
	public double getRt_avg() {
		return rt_avg;
	}
	public void setRt_avg(double rt_avg) {
		this.rt_avg = rt_avg;
	}
	public Date getRt_month() {
		return rt_month;
	}
	public void setRt_month(Date rt_month) {
		this.rt_month = rt_month;
	}
	
	
}
