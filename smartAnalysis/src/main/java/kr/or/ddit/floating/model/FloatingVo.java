package kr.or.ddit.floating.model;

public class FloatingVo {
	private int ft_num;
	private int ft_count;
	private long reg_code;
	
	public int getFt_num() {
		return ft_num;
	}
	public void setFt_num(int ft_num) {
		this.ft_num = ft_num;
	}
	public int getFt_count() {
		return ft_count;
	}
	public void setFt_count(int ft_count) {
		this.ft_count = ft_count;
	}
	public long getReg_code() {
		return reg_code;
	}
	public void setReg_code(long reg_code) {
		this.reg_code = reg_code;
	}
	
	
}
