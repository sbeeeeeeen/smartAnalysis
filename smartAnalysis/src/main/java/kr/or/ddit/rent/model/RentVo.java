package kr.or.ddit.rent.model;

import java.util.Date;

public class RentVo {
	private int rent_num;
	private int reg_gucode;
	private String rent_stairs;
	private Date rent_dt;
	private int rent_dpst;
	private int rent_jonse;
	private int rent_meter;

	public int getRent_num() {
		return rent_num;
	}

	public void setRent_num(int rent_num) {
		this.rent_num = rent_num;
	}

	public int getReg_gucode() {
		return reg_gucode;
	}

	public void setReg_gucode(int reg_gucode) {
		this.reg_gucode = reg_gucode;
	}

	public String getRent_stairs() {
		return rent_stairs;
	}

	public void setRent_stairs(String rent_stairs) {
		this.rent_stairs = rent_stairs;
	}

	public Date getRent_dt() {
		return rent_dt;
	}

	public void setRent_dt(Date rent_dt) {
		this.rent_dt = rent_dt;
	}

	public int getRent_dpst() {
		return rent_dpst;
	}

	public void setRent_dpst(int rent_dpst) {
		this.rent_dpst = rent_dpst;
	}

	public int getRent_jonse() {
		return rent_jonse;
	}

	public void setRent_jonse(int rent_jonse) {
		this.rent_jonse = rent_jonse;
	}

	public int getRent_meter() {
		return rent_meter;
	}

	public void setRent_meter(int rent_meter) {
		this.rent_meter = rent_meter;
	}

	@Override
	public String toString() {
		return "RentVo [rent_num=" + rent_num + ", reg_gucode=" + reg_gucode
				+ ", rent_stairs=" + rent_stairs + ", rent_dt=" + rent_dt
				+ ", rent_dpst=" + rent_dpst + ", rent_jonse=" + rent_jonse
				+ ", rent_meter=" + rent_meter + "]";
	}

}
