package kr.or.ddit.industry.model;

public class IndustryVo {
	private String IND_CODE;
	private String IND_L;
	private String IND_M;
	private String IND_S;
	private int IND_CNT;
	private String IND_LCODE;
	private String IND_MCODE;
	
	public String getIND_CODE() {
		return IND_CODE;
	}
	public void setIND_CODE(String iND_CODE) {
		IND_CODE = iND_CODE;
	}
	public String getIND_L() {
		return IND_L;
	}
	public void setIND_L(String iND_L) {
		IND_L = iND_L;
	}
	public String getIND_M() {
		return IND_M;
	}
	public void setIND_M(String iND_M) {
		IND_M = iND_M;
	}
	public String getIND_S() {
		return IND_S;
	}
	public void setIND_S(String iND_S) {
		IND_S = iND_S;
	}
	public int getIND_CNT() {
		return IND_CNT;
	}
	public void setIND_CNT(int iND_CNT) {
		IND_CNT = iND_CNT;
	}
	public String getIND_LCODE() {
		return IND_LCODE;
	}
	public void setIND_LCODE(String iND_LCODE) {
		IND_LCODE = iND_LCODE;
	}
	public String getIND_MCODE() {
		return IND_MCODE;
	}
	public void setIND_MCODE(String iND_MCODE) {
		IND_MCODE = iND_MCODE;
	}
}
