package kr.or.ddit.cooperative.model;

public class CooperativeVo {
	private int coop_num;
	private String mem_id;
	private char coop_div;
	private int cship_num;
	public int getCoop_num() {
		return coop_num;
	}
	public void setCoop_num(int coop_num) {
		this.coop_num = coop_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public char getCoop_div() {
		return coop_div;
	}
	public void setCoop_div(char coop_div) {
		this.coop_div = coop_div;
	}
	public int getCship_num() {
		return cship_num;
	}
	public void setCship_num(int cship_num) {
		this.cship_num = cship_num;
	}
	
	
}
